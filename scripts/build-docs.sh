</head>
<body>
<div class="container">
  <h1>Mission Control</h1>
  <p class="subtitle">Agent orchestrator documentation</p>

  <div class="section">
    <div class="section-title">Project Root</div>
    <ul class="doc-list">
      <li><a href="/docs/raw/README.md">README.md<div class="desc">Project overview, architecture, setup</div></a></li>
      <li><a href="/docs/raw/CLAUDE.md">CLAUDE.md<div class="desc">Development instructions, invariants, patterns</div></a></li>
    </ul>
  </div>

  <div class="section">
    <div class="section-title">Documentation</div>
    <ul class="doc-list" id="docs"></ul>
  </div>

  <div class="llm-links">
    <a href="/docs/llms.txt">llms.txt</a>
    <a href="/docs/llms-full.txt">llms-full.txt</a>
  </div>

  <footer>Generated by build-docs.sh</footer>
</div>
<script>
HTMLEOF

# Inject doc entries dynamically
echo "const docs = [" >> "$OUT/index.html"
for f in $(find "$DOCS" -maxdepth 1 -name "*.md" -type f | sort); do
  name="$(basename "$f")"
  title="$(head -5 "$f" | grep "^# " | head -1 | sed 's/^# //' | sed "s/'/\\\\'/g" || echo "$name")"
  echo "  {name:'$name', title:'$title'}," >> "$OUT/index.html"
done
cat >> "$OUT/index.html" << 'HTMLEOF2'
];
const ul = document.getElementById('docs');
docs.forEach(d => {
  const li = document.createElement('li');
  li.innerHTML = `<a href="/docs/raw/${d.name}">${d.name}<div class="desc">${d.title}</div></a>`;
  ul.appendChild(li);
});
</script>
</body>
</html>
HTMLEOF2

echo "[build-docs] Generated: $OUT/llms-full.txt ($(wc -c < "$OUT/llms-full.txt") bytes), llms.txt, index.html ($TOTAL_DOCS docs)"
